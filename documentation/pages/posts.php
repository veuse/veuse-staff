<h3>Create a project to go in your portfolio</h3>
<ol>
	<li>Go to <a href="post-new.php?post_type=portfolio" target="blank">Portfolio &raquo; Add New Project.</a> This creates a new project. </li>
	<li>Give the project a title and add content in the editor.</li>
	<li>Give the project a featured image. This is what will be displayed in the portfolio pages.</li>
	<li>Assign it to a portfolio (category).</li>
	<li>There is a meta-panel on the page called 'Project Meta', where you can enter some extra data for the project. Enter the data you want to be displayed.</br><br>
		
			<ul class="inline-list">
				<li><b>The Project Meta fields:</b></li>
				<li>Project website</li>
				<li>Client - Name of client</li>
				<li>Launch - Time of launch</li>
				<li>Credits</li>
			</ul>
	</li>
</ol>

