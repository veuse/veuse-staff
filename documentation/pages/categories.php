<h1>Teams</h1>
<h4>Staff-posts are called <b>Employees</b>, and are organized in categories called teams.</h4>

<h3>How to create a team</h3>
<ol>
	<li>Go to <a href="edit-tags.php?taxonomy=portfolio-category&post_type=portfolio" target="blank">Staff &raquo; Teams.</a></li>
	<li>Under the heading "Add New Team", enter a name for your portfolio and click on the button <b>Add New Team</b></li>
