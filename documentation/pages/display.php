<h1>Displaying your teams</h1>
		
<h4>There are two ways of showing your teams on your site. You can either use a shortcode, or insert via a widget ( if you have installed Page Builder plugin by SiteOrigin</h4>

<code>[veuse_staff teams="" columns=""]</code>

<h3>Attributes</h3>
<p><b>teams:</b> Enter id of the team you want to display ( Required ).</p>
<p><b>columns:</b> How many columns for the grid. Optional. Default: 3.</p>

